defined('_JEXEC') or die;

use Joomla\CMS\Form\FormField;
use Joomla\CMS\Language\Text;
use Joomla\CMS\Component\ComponentHelper;

class JFormFieldPersonalData extends FormField
{
	/**
	 * The form field type.
	 *
	 * @var    string
	 *
	 * @since  1.0.0
	 */
	protected $type = 'personaldata';

	/**
	 * Name of the layout being used to render the field
	 *
	 * @var    string
	 *
	 * @since  1.0.0
	 */
	protected $layout = 'components.com_profiles.form.personaldata';

	/**
	 * Method to get the data to be passed to the layout for rendering.
	 *
	 * @return  array
	 *
	 * @since  1.0.0
	 */
	protected function getLayoutData()
	{
		$data         = parent::getLayoutData();
		$data['text'] = Text::sprintf('COM_PROFILES_REGISTRATION_PERSONALDATA_TEXT',
			ComponentHelper::getParams('com_profiles')->get('personaldata_link'));

		return $data;
	}
}